include_once 'entidades/creditosBE.php';

class creditosDAO{

    /*
    * Altera
    * Recebe array como parametro
    */
    public function Update($dados){

        $retorno = 0;

        # Faz conex�o
        $conexao = new conexaoBanco();
        $conexao->conectar();

        # Executa comando SQL
        $stmt = $conexao->pdo->prepare('UPDATE creditos SET id_usuario = ?, valor = ?  WHERE id_credito = ?');

        # Seta Atributos nulos


        # Parametros
        $stmt->bindValue(3,$dados->getId_credito());
        $stmt->bindValue(1,$dados->getId_usuario());
        $stmt->bindValue(2,$dados->getValor());


        try{
            $retorno = $stmt->execute();
        }
        catch (PDOException $e) {
            echo 'Erro: '.$e->getMessage();
            $retorno = -1;
        }

        return $retorno;
    }
    /*
    * M�todo de Inclus�o
    * Insere dados recebendo valores via par�metro
    * ---------------------------------------------
    */
    public function incluir($dados){
        # Faz conex�o
        $conexao = new conexaoBanco();
        $conexao->conectar();

        try{
            $stmt = $conexao->pdo->prepare('INSERT INTO creditos (id_usuario, valor) VALUES (?,?)');



			$stmt->bindValue(1,$dados->getId_usuario());
			$stmt->bindValue(2,$dados->getValor());

            $retorno = $stmt->execute();
        }
        catch ( PDOException $ex ){  
            echo 'Erro: ' . $ex->getMessage(); 
        }

        return $retorno;
    }
    /*
    * Obtem por Pk
    */
    public function ObterPorPK($pk){

    	# Faz conex�o
	    $conexao = new conexaoBanco();
	    $conexao->conectar();

	    # Executa comando SQL
	    $stmt = $conexao->pdo->prepare('SELECT id_credito, id_usuario, valor FROM creditos WHERE id_credito = ? ');

	    # Passando os valores a serem usados
    	$dados = array($pk);
    	$stmt->execute($dados);
    	$retorno = $stmt->fetchAll(PDO::FETCH_ASSOC);

    	#Inst�ncia da entidade
    	$creditosBE = new creditosBE();

    	foreach( $retorno as $row ){

    		#Atribui valores
		    $creditosBE->setId_credito($row['id_credito']);
		    $creditosBE->setId_usuario($row['id_usuario']);
		    $creditosBE->setValor($row['valor']);
    	}

    	return $creditosBE;
    }
    /*
    * Obtem todos
    */
    public function ObterPorTodos(){

    	# Faz conex�o
    	$conexao = new conexaoBanco();
    	$conexao->conectar();

    	# Executa comando SQL
    	$stmt = $conexao->pdo->prepare('SELECT id_credito, id_usuario, valor FROM creditos ORDER BY id_credito DESC');

    	// Executa Query
    	$stmt->execute();
    	$retorno = $stmt->fetchAll(PDO::FETCH_ASSOC);

    	$lista = array();
    	$i = 0;

    	foreach( $retorno as $row ){
    		#Inst�ncia da entidade
    		$creditosBE = new creditosBE();

    		#Atribui valores
		    $creditosBE->setId_credito($row['id_credito']);
		    $creditosBE->setId_usuario($row['id_usuario']);
		    $creditosBE->setValor($row['valor']);

    		$lista[$i] = $creditosBE;
    		$i++;
    	}

    	return $lista;
    }
    /*
    * Delete
    * Recebe PK como parametro
	*/
    public function Deletar($pk){

		$retorno = 0;

		# Faz conex�o
		$conexao = new conexaoBanco();
		$conexao->conectar();

		# Executa SQL
		$stmt = $conexao->pdo->prepare('DELETE FROM creditos WHERE id_credito = ?');

		$dadosDelete = array($pk);

		try{
			$retorno = $stmt->execute($dadosDelete);
		}
		catch (PDOException $e) {
			echo 'Erro: '.$e->getMessage();
			$retorno = -1;
		}

		return $retorno;
	}

}
